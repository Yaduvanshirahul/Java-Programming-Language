// DATA TYPE LECTURE

public class class5{
    public static void main(String[] args)
    {
        byte a=123;                // (BYTE STORE VALUE FROM -128 TO 127) TAKE 1 BYTE
        System.out.println(a);

         short g = 224;
        System.out.println(g);    // (SHORT STORE VALUE FROM -128 TO 127) TAKE 2 BYTE

        boolean b = true;
          System.out.println(b);   // (BOOLEAN STORE VALUE TRUE AND FALSE ) TAKE 1 BYTE

        char n = 'p';
        System.out.println(n);     // (CHAR STORE VALUE FROM a TO z , A ...) TAKE 2 BYTE

        int r = 34;
         System.out.println(r);    // (INT STORE VALUE FROM -2B TO 2B) TAKE 2 BYTE

        double  c = 4.5345;
          System.out.println(c);   // (DOUBLE STORE VALUE FROM -128 TO 127) TAKE 8 BYTE

        long d = 43522476678800676l; // (LONG STORE  OF DECIMAL WITH LARGE NUMBER) TAKE 8 BYTE
          System.out.println(d);

        float e = 5.423f;               // (FLOAT STORE OF DECIMAL WITH LESS NUMBER) TAKE 4 BYTE
        System.out.println(e);
         
       

        

    }
}