//  BIT MANIPULATION

public class class1 {
    public static void main(String[] args) {

        System.out.println(5 & 7); // BINARY AND

        System.out.println(5 | 7); // BINARY OR

        System.out.println(5 ^ 7); // BINARY XOR

        System.out.println(~6);// BINARY ONE'S COMPLIMENT

        System.out.println(5 << 3);// BINARY LEFT SHIFT

        System.out.println(5 >> 2);// BINARY RIGHT SHIFT
    }
}