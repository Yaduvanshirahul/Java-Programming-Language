 // SWITCH CASE WITH THE HELP OF CHARACTER VARIABLE

 
 public class class10 {
    public static void main(String[] args) {
        
        char ch  = 'a';

        switch (ch) {
            case 'b':
                System.out.println("samosa");
                break;
            case 'a':
                System.out.println("mango shake");
                break;
                
            case 's':
                System.out.println("cold drink");
                break;    
        
            default:
                System.out.println("we don't get anything");
                break;
        }
    }
}

    

