// SWITCH CASE WITH THE HELP OF INTEGER

public class class9 {
    public static void main(String[] args) {
        
        int num = 4;

        switch (num) {
            case 11:
                System.out.println("samosa");
                break;
            case 22:
                System.out.println("mango shake");
                break;
                
            case 34:
                System.out.println("cold drink");
                break;    
        
            default:
                System.out.println("we don't get anything");
                break;
        }
    }
}
